#include <iostream>
#include <cmath>
using namespace std;

int main() {
    int n1 = 4;
    double n2 = 12.25, n3 = 0.0121;

    cout << "Raiz quadrada de " << n1 << " = " << sqrt(n1) << endl;
    cout << "Raiz quadrada de " << n2 << " = " << sqrt(n2) << endl;
    cout << "Raiz quadrada de " << n3 << " = " << sqrt(n3) << endl;

    return 0;
}
